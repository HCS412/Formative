import { useState, useMemo } from 'react'
import { Link, useNavigate } from 'react-router-dom'
import { useAuth } from '@/context/AuthContext'
import { useToast } from '@/components/ui/Toast'
import { cn } from '@/lib/utils'
import { Sparkles, User, Building2, Palette, Mail, Lock, UserCircle, ArrowRight, Check } from 'lucide-react'

const userTypes = [
  { id: 'influencer', icon: User, label: 'Creator', desc: 'Content creator or influencer' },
  { id: 'brand', icon: Building2, label: 'Brand', desc: 'Company or business' },
  { id: 'freelancer', icon: Palette, label: 'Freelancer', desc: 'Independent professional' },
]

const passwordRequirements = [
  { id: 'length', label: 'At least 8 characters', test: (p) => p.length >= 8 },
  { id: 'uppercase', label: 'One uppercase letter', test: (p) => /[A-Z]/.test(p) },
  { id: 'lowercase', label: 'One lowercase letter', test: (p) => /[a-z]/.test(p) },
  { id: 'number', label: 'One number', test: (p) => /[0-9]/.test(p) },
  { id: 'special', label: 'One special character', test: (p) => /[!@#$%^&*(),.?":{}|<>]/.test(p) },
]

export function Register() {
  const [formData, setFormData] = useState({
    name: '',
    email: '',
    password: '',
    userType: '',
  })
  const [loading, setLoading] = useState(false)
  const [showPasswordHints, setShowPasswordHints] = useState(false)
  const { register } = useAuth()
  const { addToast } = useToast()
  const navigate = useNavigate()

  const passwordStrength = useMemo(() => {
    const passed = passwordRequirements.filter(req => req.test(formData.password))
    return {
      passed,
      total: passwordRequirements.length,
      isValid: passed.length === passwordRequirements.length,
      percentage: (passed.length / passwordRequirements.length) * 100
    }
  }, [formData.password])

  const handleSubmit = async (e) => {
    e.preventDefault()

    if (!formData.userType) {
      addToast('Please select your account type', 'error')
      return
    }

    if (!passwordStrength.isValid) {
      addToast('Please meet all password requirements', 'error')
      setShowPasswordHints(true)
      return
    }

    setLoading(true)

    try {
      const result = await register(formData)
      if (result.success) {
        localStorage.setItem('userType', formData.userType)
        sessionStorage.setItem('userType', formData.userType)
        addToast('Account created! Let\'s set up your profile.', 'success')
        navigate('/onboarding')
      } else {
        if (result.requirements) {
          addToast(result.error, 'error')
          setShowPasswordHints(true)
        } else {
          addToast(result.error || 'Registration failed', 'error')
        }
      }
    } catch (error) {
      addToast(error.message || 'Registration failed', 'error')
    } finally {
      setLoading(false)
    }
  }

  return (
    <div className="min-h-screen bg-[#09090b] flex">
      {/* Left side - Decorative */}
      <div className="hidden lg:flex lg:w-1/2 relative overflow-hidden">
        <div className="absolute inset-0 bg-gradient-to-br from-violet-600/20 via-indigo-600/10 to-transparent" />
        <div className="absolute top-1/3 left-1/3 w-[500px] h-[500px] rounded-full bg-violet-600/20 blur-[100px]" />
        <div className="absolute bottom-1/3 right-1/4 w-[400px] h-[400px] rounded-full bg-indigo-600/20 blur-[80px]" />

        <div
          className="absolute inset-0 opacity-[0.03]"
          style={{
            backgroundImage: 'linear-gradient(rgba(255,255,255,0.8) 1px, transparent 1px), linear-gradient(90deg, rgba(255,255,255,0.8) 1px, transparent 1px)',
            backgroundSize: '64px 64px',
          }}
        />

        <div className="relative z-10 flex flex-col justify-center px-16">
          <Link to="/" className="flex items-center gap-3 mb-12">
            <div className="w-10 h-10 rounded-xl bg-gradient-to-br from-indigo-500 to-violet-600 flex items-center justify-center shadow-lg shadow-indigo-500/25">
              <Sparkles className="w-5 h-5 text-white" />
            </div>
            <span className="text-xl font-semibold text-white">Formative</span>
          </Link>

          <h1 className="text-4xl font-bold text-white mb-4">
            Start your journey
          </h1>
          <p className="text-xl text-zinc-400 max-w-md">
            Create an account to connect with brands, manage campaigns, and grow your creator business.
          </p>

          <div className="mt-12 space-y-4">
            {['Connect with top brands', 'Track your performance', 'Get paid securely'].map((item, i) => (
              <div key={i} className="flex items-center gap-3 text-zinc-400">
                <div className="w-5 h-5 rounded-full bg-indigo-500/20 flex items-center justify-center">
                  <Check className="w-3 h-3 text-indigo-400" />
                </div>
                {item}
              </div>
            ))}
          </div>
        </div>
      </div>

      {/* Right side - Form */}
      <div className="flex-1 flex items-center justify-center px-6 py-12 overflow-y-auto">
        <div className="w-full max-w-md">
          {/* Mobile logo */}
          <Link to="/" className="flex items-center justify-center gap-3 mb-10 lg:hidden">
            <div className="w-10 h-10 rounded-xl bg-gradient-to-br from-indigo-500 to-violet-600 flex items-center justify-center shadow-lg shadow-indigo-500/25">
              <Sparkles className="w-5 h-5 text-white" />
            </div>
            <span className="text-xl font-semibold text-white">Formative</span>
          </Link>

          {/* Form card */}
          <div className="bg-zinc-900/50 rounded-2xl p-8 border border-white/10 backdrop-blur-xl">
            <div className="text-center mb-8">
              <h2 className="text-2xl font-bold text-white mb-2">Create account</h2>
              <p className="text-zinc-400">Join the future of creator partnerships</p>
            </div>

            <form onSubmit={handleSubmit} className="space-y-6">
              {/* User Type Selection */}
              <div>
                <label className="block text-sm font-medium text-zinc-300 mb-3">I am a...</label>
                <div className="grid grid-cols-3 gap-3">
                  {userTypes.map((type) => {
                    const Icon = type.icon
                    const isSelected = formData.userType === type.id
                    return (
                      <button
                        key={type.id}
                        type="button"
                        onClick={() => setFormData(prev => ({ ...prev, userType: type.id }))}
                        className={cn(
                          'flex flex-col items-center gap-2 p-4 rounded-xl border transition-all duration-200',
                          isSelected
                            ? 'bg-indigo-500/20 border-indigo-500/50 text-white'
                            : 'bg-zinc-800/50 border-white/10 text-zinc-400 hover:border-white/20 hover:text-zinc-300'
                        )}
                      >
                        <Icon className={cn('w-6 h-6', isSelected ? 'text-indigo-400' : '')} />
                        <span className="text-sm font-medium">{type.label}</span>
                      </button>
                    )
                  })}
                </div>
              </div>

              {/* Name */}
              <div>
                <label className="block text-sm font-medium text-zinc-300 mb-2">Full Name</label>
                <div className="relative">
                  <UserCircle className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-zinc-500" />
                  <input
                    type="text"
                    value={formData.name}
                    onChange={(e) => setFormData(prev => ({ ...prev, name: e.target.value }))}
                    placeholder="Jordan Lee"
                    required
                    className="w-full h-12 pl-12 pr-4 bg-zinc-800/50 border border-white/10 rounded-xl text-white placeholder-zinc-500 focus:outline-none focus:border-indigo-500 focus:ring-2 focus:ring-indigo-500/20 transition-all"
                  />
                </div>
              </div>

              {/* Email */}
              <div>
                <label className="block text-sm font-medium text-zinc-300 mb-2">Email</label>
                <div className="relative">
                  <Mail className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-zinc-500" />
                  <input
                    type="email"
                    value={formData.email}
                    onChange={(e) => setFormData(prev => ({ ...prev, email: e.target.value }))}
                    placeholder="jordan@example.com"
                    required
                    className="w-full h-12 pl-12 pr-4 bg-zinc-800/50 border border-white/10 rounded-xl text-white placeholder-zinc-500 focus:outline-none focus:border-indigo-500 focus:ring-2 focus:ring-indigo-500/20 transition-all"
                  />
                </div>
              </div>

              {/* Password */}
              <div>
                <label className="block text-sm font-medium text-zinc-300 mb-2">Password</label>
                <div className="relative">
                  <Lock className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-zinc-500" />
                  <input
                    type="password"
                    value={formData.password}
                    onChange={(e) => setFormData(prev => ({ ...prev, password: e.target.value }))}
                    onFocus={() => setShowPasswordHints(true)}
                    placeholder="••••••••"
                    required
                    className="w-full h-12 pl-12 pr-4 bg-zinc-800/50 border border-white/10 rounded-xl text-white placeholder-zinc-500 focus:outline-none focus:border-indigo-500 focus:ring-2 focus:ring-indigo-500/20 transition-all"
                  />
                </div>

                {/* Password Strength */}
                {formData.password && (
                  <div className="mt-3">
                    <div className="h-1 bg-zinc-800 rounded-full overflow-hidden">
                      <div
                        className={cn(
                          'h-full transition-all duration-300',
                          passwordStrength.percentage < 40 ? 'bg-red-500' :
                          passwordStrength.percentage < 80 ? 'bg-amber-500' : 'bg-emerald-500'
                        )}
                        style={{ width: `${passwordStrength.percentage}%` }}
                      />
                    </div>
                  </div>
                )}

                {/* Password Requirements */}
                {showPasswordHints && (
                  <div className="mt-3 p-4 rounded-xl bg-zinc-800/50 border border-white/5">
                    <p className="text-xs text-zinc-500 mb-3">Password requirements:</p>
                    <div className="grid grid-cols-2 gap-2">
                      {passwordRequirements.map((req) => {
                        const passed = req.test(formData.password)
                        return (
                          <div
                            key={req.id}
                            className={cn(
                              'text-xs flex items-center gap-2',
                              passed ? 'text-emerald-400' : 'text-zinc-500'
                            )}
                          >
                            {passed ? (
                              <Check className="w-3 h-3" />
                            ) : (
                              <div className="w-3 h-3 rounded-full border border-current" />
                            )}
                            {req.label}
                          </div>
                        )
                      })}
                    </div>
                  </div>
                )}
              </div>

              {/* Submit */}
              <button
                type="submit"
                disabled={loading}
                className="w-full h-12 bg-white text-zinc-900 font-semibold rounded-xl hover:bg-zinc-100 transition-all flex items-center justify-center gap-2 disabled:opacity-50 disabled:cursor-not-allowed"
              >
                {loading ? (
                  <div className="w-5 h-5 border-2 border-zinc-900/20 border-t-zinc-900 rounded-full animate-spin" />
                ) : (
                  <>
                    Create Account
                    <ArrowRight className="w-5 h-5" />
                  </>
                )}
              </button>
            </form>

            {/* Divider */}
            <div className="flex items-center gap-4 my-8">
              <div className="flex-1 h-px bg-white/10" />
              <span className="text-sm text-zinc-500">or continue with</span>
              <div className="flex-1 h-px bg-white/10" />
            </div>

            {/* Social signup */}
            <div className="grid grid-cols-2 gap-3">
              <button className="h-12 flex items-center justify-center gap-2 bg-zinc-800/50 border border-white/10 rounded-xl text-zinc-300 hover:bg-zinc-800 transition-colors">
                <svg className="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
                  <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                  <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                  <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                  <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                </svg>
                Google
              </button>
              <button className="h-12 flex items-center justify-center gap-2 bg-zinc-800/50 border border-white/10 rounded-xl text-zinc-300 hover:bg-zinc-800 transition-colors">
                <svg className="w-5 h-5" viewBox="0 0 24 24" fill="currentColor">
                  <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z"/>
                </svg>
                GitHub
              </button>
            </div>

            {/* Sign in link */}
            <p className="text-center text-zinc-400 mt-8">
              Already have an account?{' '}
              <Link to="/login" className="text-indigo-400 hover:text-indigo-300 font-medium transition-colors">
                Sign in
              </Link>
            </p>
          </div>
        </div>
      </div>
    </div>
  )
}
